export function getRussianDifficultyPrompt(grade: number, level: DifficultyLevel): string {
  if (grade <= 2) return RUSSIAN_1_2[level]
  if (grade <= 4) return RUSSIAN_3_4[level]
  if (grade <= 6) return RUSSIAN_5_6[level]
  if (grade <= 8) return RUSSIAN_7_8[level]
  if (grade === 9) return RUSSIAN_9[level]
  if (grade === 10) return RUSSIAN_10[level]
  return RUSSIAN_11[level]
}
